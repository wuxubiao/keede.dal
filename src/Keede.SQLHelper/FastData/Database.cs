    /// <summary>
    /// A simple helper class for build SQL statements
    /// </summary>
    public class Sql
    {
        /// <summary>
        /// Default, empty constructor
        /// </summary>
        public Sql()
        {
        }

        /// <summary>
        /// Construct an SQL statement with the supplied SQL and arguments
        /// </summary>
        /// <param name="sql">The SQL statement or fragment</param>
        /// <param name="args">Arguments to any parameters embedded in the SQL</param>
        public Sql(string sql, params object[] args)
        {
            _sql = sql;
            _args = args;
        }

        /// <summary>
        /// Instantiate a new SQL Builder object.  Weirdly implemented as a property but makes
        /// for more elegantly readble fluent style construction of SQL Statements
        /// eg: db.Query(Sql.Builder.Append(....))
        /// </summary>
        public static Sql Builder
        {
            get { return new Sql(); }
        }

        readonly string _sql;
        readonly object[] _args;
        Sql _rhs;
        string _sqlFinal;
        object[] _argsFinal;
        static readonly DatabaseType _dbType = new SqlServerDatabaseType();

        private void Build()
        {
            // already built?
            if (_sqlFinal != null)
                return;

            // Build it
            var sb = new StringBuilder();
            var args = new List<object>();
            Build(sb, args, null);
            _sqlFinal = sb.ToString();
            _argsFinal = args.ToArray();
        }

        /// <summary>
        /// 
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <returns></returns>
        public static string BuildInsert<T>(T poco, string paramPrefix = "@")
        {
            var names = new List<string>();
            var values = new List<string>();
            var p = PocoData.ForType(poco.GetType());
            var pd = PocoData.ForObject(poco, p.TableInfo.PrimaryKey);
            foreach (var i in pd.Columns)
            {
                if (i.Value.ResultColumn)
                    continue;
                names.Add(_dbType.EscapeSqlIdentifier(i.Key));
                values.Add(string.Format("{0}{1}", paramPrefix, i.Value.ColumnName));
            }
            return string.Format("INSERT INTO {0} ({1}){2} VALUES ({3})",
                                    _dbType.EscapeTableName(pd.TableInfo.TableName),
                                    string.Join(",", names.ToArray()),
                                    string.Empty,
                                    string.Join(",", values.ToArray())
                                    );
        }

        /// <summary>
        /// 
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <param name="poco"></param>
        /// <param name="paramPrefix"></param>
        /// <returns></returns>
        public static string BuildUpdate<T>(T poco, string paramPrefix = "@")
        {
            object primaryKeyValue = null;
            var sb = new StringBuilder();
            var p = PocoData.ForType(poco.GetType());
            var pd = PocoData.ForObject(poco, p.TableInfo.PrimaryKey);
            int index = 0;
            foreach (var i in pd.Columns)
            {
                // Don't update the primary key, but grab the value if we don't have it
                if (String.Compare(i.Key, pd.TableInfo.PrimaryKey, StringComparison.OrdinalIgnoreCase) == 0)
                {
                    if (primaryKeyValue == null)
                        primaryKeyValue = i.Value.GetValue(poco);
                    continue;
                }

                // Dont update result only columns
                if (i.Value.ResultColumn)
                    continue;

                // Build the sql
                if (index > 0)
                    sb.Append(", ");
                index++;
                sb.AppendFormat("{0} = {1}{2}", _dbType.EscapeSqlIdentifier(i.Key), paramPrefix, i.Value.ColumnName);
            }
            return string.Format("UPDATE {0} SET {1} WHERE {2} = {3}{4}",
                                            _dbType.EscapeTableName(pd.TableInfo.TableName), sb, _dbType.EscapeSqlIdentifier(pd.TableInfo.PrimaryKey), paramPrefix, pd.TableInfo.PrimaryKey);
        }

        /// <summary>
        /// 
        /// </summary>
        /// <typeparam name="T"></typeparam>
        /// <returns></returns>
        public static string BuildDelete<T>()
        {
            var p = PocoData.ForType(typeof(T));
            return string.Format("DELETE FROM {0} WHERE {1}=@{2}", _dbType.EscapeTableName(p.TableInfo.TableName), _dbType.EscapeSqlIdentifier(p.TableInfo.PrimaryKey), p.TableInfo.PrimaryKey);

        }

        /// <summary>
        /// Returns the final SQL statement represented by this builder
        /// </summary>
        public string SQL
        {
            get
            {
                Build();
                return _sqlFinal;
            }
        }

        /// <summary>
        /// Gets the complete, final set of arguments collected by this builder.
        /// </summary>
        public object[] Arguments
        {
            get
            {
                Build();
                return _argsFinal;
            }
        }

        /// <summary>
        /// Append another SQL builder instance to the right-hand-side of this SQL builder
        /// </summary>
        /// <param name="sql">A reference to another SQL builder instance</param>
        /// <returns>A reference to this builder, allowing for fluent style concatenation</returns>
        public Sql Append(Sql sql)
        {
            if (_rhs != null)
                _rhs.Append(sql);
            else
                _rhs = sql;

            return this;
        }

        /// <summary>
        /// Append an SQL fragement to the right-hand-side of this SQL builder
        /// </summary>
        /// <param name="sql">The SQL statement or fragment</param>
        /// <param name="args">Arguments to any parameters embedded in the SQL</param>
        /// <returns>A reference to this builder, allowing for fluent style concatenation</returns>
        public Sql Append(string sql, params object[] args)
        {
            return Append(new Sql(sql, args));
        }

        static bool Is(Sql sql, string sqltype)
        {
            return sql != null && sql._sql != null && sql._sql.StartsWith(sqltype, StringComparison.InvariantCultureIgnoreCase);
        }

        private void Build(StringBuilder sb, List<object> args, Sql lhs)
        {
            if (!String.IsNullOrEmpty(_sql))
            {
                // Add SQL to the string
                if (sb.Length > 0)
                {
                    sb.Append("\n");
                }

                var sql = ParametersHelper.ProcessParams(_sql, _args, args);

                if (Is(lhs, "WHERE ") && Is(this, "WHERE "))
                    sql = "AND " + sql.Substring(6);
                if (Is(lhs, "ORDER BY ") && Is(this, "ORDER BY "))
                    sql = ", " + sql.Substring(9);

                sb.Append(sql);
            }

            // Now do rhs
            if (_rhs != null)
                _rhs.Build(sb, args, this);
        }

