        /// <returns>A TableInfo instance</returns>
        public static TableInfo FromPoco(Type t)
        {
            TableInfo ti = new TableInfo();

            // Get the table name
            var a = t.GetCustomAttributes(typeof(TableNameAttribute), true);
            ti.TableName = a.Length == 0 ? t.Name : ((TableNameAttribute)a[0]).Value;

            // Get the primary key
            a = t.GetCustomAttributes(typeof(PrimaryKeyAttribute), true);
            ti.PrimaryKey = a.Length == 0 ? "ID" : ((PrimaryKeyAttribute)a[0]).Value;
            ti.SequenceName = a.Length == 0 ? null : ((PrimaryKeyAttribute)a[0]).SequenceName;

            return ti;
        }
